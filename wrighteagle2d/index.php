<h2>Introduction</h2>
<p>WrightEagle 2D Soccer Simulation Team was established in 1998. It is a branch of WrightEagle Robocup Team. WrightEagle RoboCup Team belongs to 
<a href="http://wrighteagle.org" target="_blank">Multi-Agent Systems Lab.</a>, <a href="http://cs.ustc.edu.cn/" target="_blank">School of Compute Science and Technology</a>, <a href="http://www.ustc.edu.cn" target="_blank">University of Science and Technology of China</a>, China.</p>
<p>
We have participated in annual competitions of RoboCup since 1999 and have got 4 champions and 5 runners-up of RoboCup in recent 9 years. We take RoboCup 2D 
soccer simulation as a typical problem of multi-agent systems, and our long-term goal is to do research in decision-making and other challenging projects in Artificial Intelligence.
</p>
